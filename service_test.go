func testServiceOutput(t *testing.T, filePath string, expectedOutput string) {
	reader, err := os.Open(filePath)
	if err != nil {
		t.Fatal("Could not read JSON file...")
	}
	defer reader.Close()

	products, err := parseProducts(reader)
	if err != nil {
		t.Errorf(err.Error())
	}
	if len(products) != 1 {
		t.Fatalf("Unexpected number of products found. Got %d, expected %d", len(products), 1)
	}

	buf := bytes.Buffer{}
	dumpRepositories(&buf, products[0])

	result := buf.String()
	if result != expectedOutput {
		t.Errorf(result)
	}
}

